#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*arr;

	i = 0;
	arr = s;
	if (n != 0)
	{
		while (i < n)
		{
			arr[i] = 0;
			i++;
		}
	}
}
